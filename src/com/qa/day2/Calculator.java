package com.qa.day2;

public class Calculator {

	public int add(int a, int b) {
		return a+b;
	}
	
	public int sub(int a, int b) {
		return a-b;
	}
	
	public double divide(double a, double b) {
		return a/b;//3.56 - 3, doubles - return a decimal
	}
	
	public int multiply(int a, int b) {
		return a*b;
	}
	
}
